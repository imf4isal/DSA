public class Conditionals {
    public static void main(String[] args) {
        int i = 10;
        if(i > 5) {
            System.out.println("Number is greater than 5");
        } else if (i == 10) {
            System.out.println("Number is equal to 10");

        } else {
            System.out.println("No number");
        }
    }
}
